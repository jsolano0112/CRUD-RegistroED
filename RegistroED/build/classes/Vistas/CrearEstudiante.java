    //SE GUARDA LOS DATOS DEL ESTUDIANTE
    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        
//        JOptionPane.showMessageDialog(this, "Guardado.");
        
        try {
            Estudiante alumno = new Estudiante();
//            alumno.setIdA(Integer.parseInt(txtid.getValue().toString()));
            alumno.setIdA(txtid.getText());
            alumno.setNombresA(txtnombres.getText());
            alumno.setpApellidoA(txtprimerap1.getText());
            alumno.setsApellidoA(txtsegundoap.getText());
            alumno.setCorreoA(txtcorreo.getText());
            alumno.setContraseñaA(txtclave.getText());
            
            EstudianteDao esdao = new EstudianteDao();
            
          if(esdao.crearEstudiante(alumno)){
                JOptionPane.showMessageDialog(this, "Estudiante registrado.");
                this.limpiar();
            }else{
                 JOptionPane.showMessageDialog(this, "ERROR AL GUARDAR");
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnguardarActionPerformed
